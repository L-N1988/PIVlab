function mbinfo
%MBINFO Information on Morris minimum-bandwidth orthogonal wavelets
% Morris minimum-bandwidth orthogonal wavelets are identified as 'mbN.L' 
% where N is the number of filter coefficients (taps) and L is the
% level of the discrete wavelet transform used in the optimization. 
% Valid options for WNAME are:
%
%  'mb4.2'
%  'mb8.2'
%  'mb8.3'
%  'mb8.4'
%  'mb10.3'
%  'mb12.3'
%  'mb14.3'
%  'mb16.3'
%  'mb18.3'
%  'mb24.3'
%  'mb32.3'
%
%   Family                          Morris minimum-bandwidth orthogonal
%                                   wavelets
%   Short name                      mb
%   Examples                        'mb4.2', 'mb14.3'
%
%   Orthogonal                      yes
%   Compact support                 yes
%   Use in all discrete wavelet 
%   and wavelet packet transforms 
%
% Morris, Joel M, and Ravindra Peravali. “Minimum-Bandwidth Discrete-Time Wavelets.”
% Signal Processing 76, no. 2 (1999): 181–93. 
% https://doi.org/10.1016/s0165-1684(99)00007-9.

%   Copyright 2021 The MathWorks, Inc.

end
